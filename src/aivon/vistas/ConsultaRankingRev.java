    private void armaCabeceraTabla() {
        //Titulos de Columnas
        ArrayList<Object> columnas = new ArrayList<>();
        columnas.add("ID");
        columnas.add("Revendedor");
        columnas.add("Nivel");
        columnas.add("Estrellas");
        columnas.add("Ganancias");
        
        columnas.forEach((it) -> {
            modelo.addColumn(it);
        });
        jt_revendedores.setModel(modelo);
    }
    
    private void borraFilasTabla() {
        int a = modelo.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            modelo.removeRow(i);
        }
    }
    
    private void cargaRevendedores() {
        this.borraFilasTabla();

        List<Revendedor> lista = rd.buscarRevendedores();
        lista.forEach((r) -> {
            String nombre = r.getNombre()+" "+r.getApellido();
            //modelo.addRow(new Object[]{r.getId_revendedor(), nombre, rd.calcularNivelRevendedorMejorado(r), rd.estrellasTotalesRevendedor(r), rd.gananciaFinalRevendedor(r)});
            modelo.addRow(new Object[]{r.getId_revendedor(), nombre, rd.calcularNivelRevendedorMejorado(r), rd.estrellasTotalesRevendedor(r), rd.gananciaFinalRevendedor(r)});
        });
        // Instanciamos el TableRowSorter y lo a√±adimos al JTable
            TableRowSorter<TableModel> elQueOrdena = new TableRowSorter<>(modelo);
            
            jt_revendedores.setRowSorter(elQueOrdena);
    }



